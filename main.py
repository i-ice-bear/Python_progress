basic_array = np.array([1, 2, 3, 4, 5])
print(basic_array)

# tow dimensional array or three-dimensional array.
three_dimensional_array = np.array([1, 2, 3, 4, 5], dtype=complex)
print(three_dimensional_array)

# numpy arrange;
numpy_arrange = np.arange(10,50,100)
print(numpy_arrange)
