capital_value = eval(input("Enter your capital : "))
liability_value = eval(input("Enter your liability : "))

if sum(assets_value) == sum(capital_value) + sum(liability_value):
    print("Accounting equation is matched")
else:
    print("Accounting equation is not matched properly!")

